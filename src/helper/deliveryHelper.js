export function getAvailableTimes(deliveryDate) {
  if (!deliveryDate) return ALL_TIMES;
  const today = new Date().toISOString().split("T")[0];
  if (deliveryDate !== today) return ALL_TIMES;
  const now = new Date();
  const cutoffHour = now.getHours() + 5;
  const currentMinutes = now.getMinutes();
  return ALL_TIMES.filter((timeStr) => {
    const [time, period] = timeStr.split(" ");
    let [hours, minutes] = time.split(":").map(Number);
    if (period === "PM" && hours !== 12) hours += 12;
    if (period === "AM" && hours === 12) hours = 0;
    if (hours > cutoffHour) return true;
    if (hours === cutoffHour && minutes >= currentMinutes) return true;
    return false;
  });
}

export function detectZone(address) {
  if (!address) return "";

  const addressLower = address.toLowerCase().trim();
  const tokens = addressLower
    .split(/[,\s]+/)
    .map((t) => t.trim())
    .filter(Boolean);

  let bestMatch = null;
  let bestScore = 0;

  for (const z of zoneKeywords) {
    for (const keyword of z.keywords) {
      const normalizedKeyword = keyword.toLowerCase().trim();

      // Skip if keyword is too generic (just city name)
      if (
        normalizedKeyword === "talisay" ||
        normalizedKeyword === "naga" ||
        normalizedKeyword === "minglanilla" ||
        normalizedKeyword === "cebu"
      ) {
        continue;
      }

      // Calculate match score
      const score = calculateMatchScore(
        addressLower,
        normalizedKeyword,
        tokens,
      );

      if (score > bestScore) {
        bestScore = score;
        bestMatch = z.zoneName;
      }
    }
  }

  // Fallback: Check for city name + mountain keywords
  if (!bestMatch && addressLower.includes("talisay")) {
    if (hasMountainKeywords(addressLower)) {
      bestMatch = "Talisay-Mountain";
    } else {
      bestMatch = "Talisay-Proper";
    }
  }

  return bestMatch ?? "";
}

